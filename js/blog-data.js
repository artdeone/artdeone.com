// Utility functions
function getRandomPosts(postsArray, count) {
    const shuffled = [...postsArray].sort(() => 0.5 - Math.random());
    return shuffled.slice(0, count);
}

function formatDate(dateString) {
    const date = new Date(dateString);
    const options = { month: 'short', day: 'numeric' };
    return date.toLocaleDateString('en-US', options);
}

function getCategory(postId) {
    const categories = {
        1: 'RESOURCE',
        2: 'THEORY', 
        3: 'THEORY',
        4: 'DESIGN',
        5: 'DESIGN',
        6: 'DESIGN',
        7: 'HISTORY',
        8: 'TUTORIAL',
        9: 'AI'
        // Add more categories for new posts
    };
    return categories[postId] || 'ARTICLE';
}

// Auto-detect post ID from URL
function getCurrentPostId() {
    // Extract post ID from URL path
    const path = window.location.pathname;
    const match = path.match(/post-(\d+)\.html$/);
    return match ? parseInt(match[1]) : null;
}

// Main function to load related posts
function loadRelatedPosts() {
    const currentPostId = getCurrentPostId();
    if (!currentPostId) return;
    
    const relatedPostsContainer = document.getElementById('related-posts');
    if (!relatedPostsContainer) return;
    
    // Filter out the current post and get 3 random posts
    const availablePosts = blogPosts.filter(post => post.id !== currentPostId);
    const randomPosts = getRandomPosts(availablePosts, 3);
    
    // Generate HTML for related posts
    randomPosts.forEach(post => {
        const postHTML = `
            <div class="bg-white rounded-lg shadow-sm hover:shadow-md transition-shadow duration-300">
                <a href="${post.file}" class="block">
                    <img src="${post.image}" alt="${post.title}" class="w-full h-48 object-cover rounded-t-lg">
                    <div class="p-4">
                        <div class="flex items-center text-sm text-gray-500 mb-2">
                            <span class="mr-3">${formatDate(post.date)}</span>
                            <span class="bg-gray-100 text-gray-700 px-2 py-1 rounded text-xs">${getCategory(post.id)}</span>
                        </div>
                        <h3 class="text-lg font-semibold text-gray-900 mb-2">${post.title}</h3>
                        <p class="text-gray-600 text-sm mb-3">${post.excerpt}</p>
                        <div class="flex items-center text-gray-500 text-sm">
                            <div class="w-6 h-6 bg-gray-300 rounded-full flex items-center justify-center mr-2">
                                <span class="text-xs font-semibold">AD</span>
                            </div>
                            <span>ART de ONE Team</span>
                        </div>
                    </div>
                </a>
            </div>
        `;
        relatedPostsContainer.innerHTML += postHTML;
    });
}